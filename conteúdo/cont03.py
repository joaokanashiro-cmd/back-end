n= input('digite algo')
print(n.isnumeric())#para perguntar se é se um número
print(n.isalpha())# para perguntar se é letra
print(n.isalnum())#para perguntar se é letra ou número
print(n.isupper())#para perguntar se é so com letra
print(n.isdecimal())# para verificr se é decimal 
print(n.isdigit())#para verificar se é um digito
print(n.islower())#para verificar se é apenas letra minusculas
print(n.isidentifier())#para verificar se é um identificador valido
print(n.isspace())#para verificar se tem um espaço em branco
